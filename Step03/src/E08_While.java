/**
 * 이 클래스는 while 반복문과 if 조건문을 결합하여 특정 조건의 숫자를 필터링하는 예제입니다.
 * 1부터 100까지의 숫자 중 4 또는 7의 배수를 찾는 로직을 구현합니다.
 */
public class E08_While {
	/*
	 * 문제: 1~100까지의 숫자들 중에 4와 7의 배수를 출력하세요.
	 * 단, 4와 7의 공배수(예: 28)는 한 번만 출력되어야 합니다.
	 * 
	 * 출력 결과 예시: 4 7 8 12 14 16 20 21 24 28 32 35 ... 98 100
	 */
	public static void main(String[] args) {
		// 1. 반복을 위한 카운터 변수 i를 1로 초기화
		int i = 1;
		
		// 2. i가 100이 될 때까지 반복 (i < 101 또는 i <= 100)
		while(i < 101) {
			// 3. 조건 판별: i가 4의 배수이거나(||) 7의 배수인 경우
			// % 연산자(나머지 연산자)를 사용하여 나머지가 0이면 배수로 판단합니다.
			// 논리합(||) 연산자를 사용하므로 공배수의 경우에도 조건이 한 번만 참이 되어 중복 출력을 방지합니다.
			if(i % 4 == 0 || i % 7 == 0) {
				System.out.print(i + " ");
			}
			
			// 4. 다음 숫자로 넘어가기 위해 i를 1 증가 (무한 루프 방지)
			i++;
		}
		// 모든 출력이 끝난 후 줄바꿈
		System.out.println();
	}

}
