/**
 * 이 클래스는 for 반복문의 기본 구조와 사용법을 학습하는 예제입니다.
 * while문과 달리 초기화, 조건식, 증감식을 한 줄에 작성하여 가독성을 높인 반복문입니다.
 */
public class E09_For {
	/*
	 * for문의 구조:
	 * 	for(초기화식; 조건식; 증감식) {
	 * 		// 조건식이 참(true)일 동안 반복 실행될 코드
	 * 	}
	 * 
	 * 실행 순서:
	 * 1. 초기화식 실행 (최초 1회)
	 * 2. 조건식 판별 (참이면 블록 실행, 거짓이면 종료)
	 * 3. 블록 내 코드 실행
	 * 4. 증감식 실행
	 * 5. 다시 2번 조건식 판별로 이동
	 */
	public static void main(String[] args) {
		// 1부터 10까지 숫자를 출력하는 for문
		// int i = 1 : 초기화식 (i는 for문 블록 안에서만 유효한 지역 변수)
		// i < 11   : 조건식 (i가 11보다 작을 때까지만 반복)
		// i++      : 증감식 (한 번의 반복이 끝날 때마다 i를 1씩 증가)
		for(int i = 1; i < 11; i++) {
			System.out.print(i + " ");
		}
		System.out.println(); // 줄바꿈
		
		// [참고] 변수의 스코프(Scope)
		// for문의 초기화식에서 선언된 변수 i는 for문 외부에서는 사용할 수 없습니다.
		// 아래 코드의 주석을 해제하면 컴파일 에러가 발생합니다.
		// System.out.println(i); 
	}

}
