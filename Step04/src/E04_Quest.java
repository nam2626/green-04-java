/**
 * 이 클래스는 배열의 요소를 순회하며 특정 조건(홀수)에 맞는 데이터의 개수를 세는 연습 예제입니다.
 */
public class E04_Quest {

	public static void main(String[] args) {
		// 1. 다양한 정수값이 저장된 배열 선언 및 초기화
		int[] arr = new int[] {23, 54, 34, 234, 6, 7, 237, 643, 111, 33};
		
		// 2. 홀수의 개수를 저장할 카운터 변수 초기화
		int count = 0;
		
		/*
		 * [문제] 배열에 저장된 값들 중 홀수의 개수를 출력하세요.
		 * 
		 * 로직 설계:
		 * - for문을 사용하여 배열의 첫 번째 요소(인덱스 0)부터 마지막 요소까지 순회합니다.
		 * - 각 요소를 2로 나눈 나머지가 1인지 확인하여 홀수 여부를 판별합니다.
		 */
		for(int i = 0; i < arr.length; i++) {
			// n % 2 == 1 : 홀수 판별 조건
			if(arr[i] % 2 == 1) {
				// 홀수인 경우 카운터 변수 1 증가
				count++;
			}
		}
		
		// 3. 최종 결과 출력
		System.out.println("배열 내 홀수의 개수 : " + count);
	}

}
