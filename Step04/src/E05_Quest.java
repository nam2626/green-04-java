/**
 * 이 클래스는 하나의 반복문만을 사용하여 배열의 여러 가지 통계값(총합, 최댓값, 최솟값)을
 * 효율적으로 산출하는 방법을 학습하는 예제입니다.
 */
public class E05_Quest {
	/*
	 * [문제]
	 * 1. 배열에 저장된 값의 총합을 출력하세요.
	 * 2. 배열에 저장된 값들 중 가장 큰 값을 출력하세요.
	 * 3. 배열에 저장된 값들 중 가장 작은 값을 출력하세요.
	 * 
	 * [조건] 반복문은 단 1개만 사용할 것.
	 */
	public static void main(String[] args) {
		// 1. 테스트용 정수 배열 선언 및 초기화
		int[] arr = new int[] {23, 54, 34, 234, 6, 7, 237, 643, 111, 33};
		
		// 2. 변수 초기화
		// 모든 변수를 배열의 첫 번째 요소(arr[0])의 값으로 초기화합니다.
		// 이렇게 하면 배열 내의 실제 값들과 비교를 시작할 수 있습니다.
		int total = arr[0];
		int max = arr[0];
		int min = arr[0];
		
		// 3. 단일 반복문을 사용하여 통계값 산출
		// i를 1부터 시작하여 배열의 나머지 요소들을 순회합니다. (0번은 이미 초기값으로 사용함)
		for(int i = 1; i < arr.length; i++) {
			// (1) 총합 누적
			total += arr[i];
			
			// (2) 최댓값 갱신: 현재 들고 있는 max보다 더 큰 값이 나타나면 교체
			if(max < arr[i]) {
				max = arr[i];
			}
			
			// (3) 최솟값 갱신: 현재 들고 있는 min보다 더 작은 값이 나타나면 교체
			if(min > arr[i]) {
				min = arr[i];
			}
		}
		
		// 4. 결과 출력
		System.out.println("총합 : " + total);
		System.out.println("최대값 : " + max);
		System.out.println("최소값 : " + min);
	}

}
